class NepaliDate extends AbstractDate implements YearMonthDate<NepaliDate> {
  NepaliDate(super.year, super.month, super.dayOfMonth);

  factory NepaliDate.fromDate(AbstractDate date) =>
      NepaliDate.fromJdn(date.toJdn());

  factory NepaliDate.fromJdn(int jdn) {
    var days = jdn - _jdSupportStart;
    final eras = _calculateEras(days, _supportedDays);
    days -= eras * _supportedDays;
    final eraStartYear = _supportedYearsStart + eras * _supportedYears;

    var index = (days / 31.26)
        .toInt(); // average months length in this calendar
    while (index + 1 < _months.length && _months[index + 1] <= days) {
      index++;
    }

    return NepaliDate(
      (index ~/ 12) + eraStartYear,
      (index % 12) + 1,
      days - _months[index] + 1,
    );
  }

  static int _calculateEras(int days, int eraLength) =>
      (days / eraLength).floor();

  @override
  int toJdn() {
    var yearIndex = year - _supportedYearsStart;
    final eras = _calculateEras(yearIndex, _supportedYears);
    yearIndex -= eras * _supportedYears;
    final erasDays = eras * _supportedDays;
    return _months[yearIndex * 12 + month - 1] +
        dayOfMonth +
        erasDays +
        _jdSupportStart -
        1;
  }

  @override
  NepaliDate monthStartOfMonthsDistance(int monthsDistance) =>
      TwelveMonthsYear.monthStartOfMonthsDistance<NepaliDate>(
        this,
        monthsDistance,
        (y, m, d) => NepaliDate(y, m, d),
      );

  @override
  int monthsDistanceTo(NepaliDate date) =>
      TwelveMonthsYear.monthsDistanceTo(this, date);

  @override
  String toString() => 'NepaliDate($year, $month, $dayOfMonth)';

