  static int toJdn(int year, int month, int day) {
    final yearIndex = year - _supportedYearsStart;
    if (yearIndex < 0 || yearIndex >= _supportedYears) return -1;
    return _months[yearIndex * 12 + month - 1] + day + _jdSupportStart - 1;
  }

  static DateTriplet? fromJdn(int jd) {
    if (jd < _jdSupportStart || jd >= _jdSupportEnd) return null;
